package Array;

public class variable_swap {

	public static void main(String[] args) {
		int a=50;
		int b=40;
		
		System.out.println("****Before swaping****");
		System.out.println(a); 
		System.out.println(b);
		
		System.out.println("****After swaping****");
		int c ;
		c=a;
	    a=b;
		b=c;
		System.out.println(a);
		System.out.println(b); 


	}
}
